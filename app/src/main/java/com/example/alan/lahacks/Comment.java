package com.example.alan.lahacks;

public class Comment {
    private String name;
    private String comm;
    private int image;
    private int id;

    public Comment(int id, String name, String comm, int image){
        this.name = name;
        this.comm = comm;
        this.id = id;
        this.image = image;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getComm() {
        return comm;
    }

    public void setComm(String comm) {
        this.comm = comm;
    }

    public int getImage() {
        return image;
    }

    public void setImage(int image) {
        this.image = image;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
}
